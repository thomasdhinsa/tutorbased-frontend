<script>
import axios from "axios";

export default {
  data: function () {
    return {
      newUserParams: {},
      errors: [],
    };
  },
  methods: {
    submit: function () {
      axios
        .post("/users", this.newUserParams)
        .then((response) => {
          console.log(response.data);
          this.$router.push("/login");
        })
        .catch((error) => {
          this.errors = error.response.data.errors;
        });
    },
  },
};
</script>

<template>
  <div class="signup">
    <form v-on:submit.prevent="submit()">
      <h1>Signup</h1>
      <ul>
        <li v-for="error in errors" v-bind:key="error">{{ error }}</li>
      </ul>
      <div>
        <label>Name:</label>
        <input type="text" v-model="newUserParams.name" />
      </div>
      <div>
        <label>Email:</label>
        <input type="email" v-model="newUserParams.email" />
      </div>
      <div>
        <label>Password:</label>
        <input type="password" v-model="newUserParams.password" />
      </div>
      <div>
        <label>Password confirmation:</label>
        <input type="password" v-model="newUserParams.password_confirmation" />
      </div>
      <div>
        <label>Is_teacher:</label>
        <input type="boolean" v-model="newUserParams.is_teacher" />
      </div>
      <div>
        <label>education:</label>
        <input type="text" v-model="newUserParams.education" />
      </div>
      <div>
        <label>zipcode:</label>
        <input type="text" v-model="newUserParams.zipcode" />
      </div>
      <div>
        <label>bio:</label>
        <input type="text" v-model="newUserParams.bio" />
      </div>
      <div>
        <label>Subject(s):</label>
        <input type="text" v-model="newUserParams.subjects" />
      </div>
      <div>
        <label>preferred_contact:</label>
        <input type="text" v-model="newUserParams.preferred_contact" />
      </div>
      <div>
        <label>image_url:</label>
        <input type="text" v-model="newUserParams.image_url" />
      </div>
      <input type="submit" value="Submit" />
    </form>
  </div>
</template>
